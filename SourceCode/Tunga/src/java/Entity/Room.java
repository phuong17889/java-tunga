package Entity;

/**
 *
 * @author Hoangha.FPT
 */
public class Room {
    private int id;
    private String name;
    private boolean type;

    public Room(int id, String name, boolean type) {
        this.id = id;
        this.name = name;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isType() {
        return type;
    }

    public void setType(boolean type) {
        this.type = type;
    }

   
}
