package Entity;

/**
 *
 * @author Hoangha.FPT
 */
public class Menu {
    private int Id;
    private String Name;

    public Menu(int Id, String Name) {
        this.Id = Id;
        this.Name = Name;
    }

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }
    
    
}
